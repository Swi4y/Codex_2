# === 2. /start и меню ===========================================
@dp.message(CommandStart())
async def cmd_start(message: types.Message):
    kb = types.ReplyKeyboardMarkup(resize_keyboard=True)
    for key in GUIDES:
        kb.add(types.KeyboardButton(text=key))
    await message.answer("Выберите нужный гайд:", reply_markup=kb)

# === 3. Обработка нажатия кнопки ================================
@dp.message()
async def send_guide(message: types.Message):
    guide = GUIDES.get(message.text)
    if not guide:
        await message.reply("Пожалуйста, нажмите одну из кнопок в меню.")
        return

    # текст-инструкция
    await message.answer(guide["text"], parse_mode="Markdown")

    # документы
    for file_path in guide["docs"]:
        if not Path(file_path).exists():
            await message.answer(f"⚠️ Файл {file_path} не найден на сервере.")
            continue
        await message.answer_document(
            types.FSInputFile(file_path),
            caption=Path(file_path).name,
        )

